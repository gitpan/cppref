=head1 NAME

App::Cppref - man-style access to cppreference.com documents (using w3m)

=head1 DESCRIPTION

This is a dummy module so that cppref can be installed using cpan, like:

  # cpan -i App::Cppref

Please refere to L<cppref> for how to use the application.

